#!/usr/bin/env bash
# Regenerate the PKM UAPI Lua binding from the C headers.
#
# The headers in pkm/uapi/pkm/ are the single source of truth for the PKM
# ABI. This script mirrors them into one committed, pure-Lua file:
#
#   pkm.lua   a module returning a table M with:
#               * every object-like #define as M.NAME, evaluated by the C
#                 compiler (so _IOWR(...) ioctl encodings, shift macros and
#                 cross-macro references all come out as plain values);
#               * M.struct, a table of struct layouts. Lua has no C structs,
#                 so the useful mirror is a Lua 5.4 string.pack format string
#                 per struct — exactly what a raw vm:syscall test feeds to
#                 provium:pack / provium:unpack. Field offsets and the total
#                 size come from the C compiler (offsetof/sizeof), the same
#                 ground truth the Go and Rust backends use, so the format is
#                 byte-identical to the kernel's view by construction.
#
# Coverage is header-driven — every `struct NAME {` and every `#define NAME`
# is picked up — so a new ABI item cannot be silently left unbound. The
# output carries the standard "DO NOT EDIT" banner.
#
# The format strings are little-endian (`<`) and carry explicit `x` padding
# computed from offsetof, so they do not lean on string.pack's own alignment
# engine: alignment is effectively 1 and every byte is accounted for. This
# targets the guest's native x86-64 ABI, which is what the agent's Syscall
# op marshals into the kernel.
#
# If a Lua 5.4 interpreter (lua5.4 / lua) is on PATH, the generated file is
# self-checked: string.packsize(entry.pack) must equal entry.size for every
# struct. luajit is skipped (Lua 5.1 — no string.pack, no 64-bit integers).
#
# Usage:
#   gen.sh            regenerate pkm.lua in place
#   gen.sh --check    regenerate into a temp dir and diff against the
#                     committed file; exit non-zero if it differs and write
#                     nothing. This is the CI drift gate.
set -euo pipefail

mode="generate"
case "${1:-}" in
	--check) mode="check" ;;
	"")      ;;
	*)       echo "usage: gen.sh [--check]" >&2; exit 2 ;;
esac

here="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"   # pkm/uapi/generated/lua
uapi="$(cd "$here/../.." && pwd)"                         # pkm/uapi
hdrs="$uapi/pkm"
cc="${CC:-cc}"
banner="-- Code generated by gen.sh; DO NOT EDIT."

# Precondition: a header outside the codegen-safe subset can carry a member
# whose width or layout we cannot mirror faithfully. Fail before that.
bash "$uapi/check-codegen-safe.sh" >/dev/null

tmp="$(mktemp -d)"
trap 'rm -rf "$tmp"' EXIT

structs="$(grep -hE '^struct[ \t]+[A-Za-z_][A-Za-z0-9_]*[ \t]*\{' "$hdrs"/*.h \
	| sed -E 's/^struct[ \t]+([A-Za-z_][A-Za-z0-9_]*).*/\1/' | LC_ALL=C sort -u)"

# --- macros.txt : object-like #define names, classified INT/STR -----------
# Same extraction as the Go and Rust backends: object-like macros only,
# skipping function-like macros and the _UAPI_* include guards.
awk '
{
	line = $0
	while (line ~ /\\[ \t]*$/) {
		sub(/\\[ \t]*$/, "", line)
		if ((getline cont) <= 0) break
		line = line cont
	}
	if (line !~ /^[ \t]*#[ \t]*define[ \t]/) next
	sub(/^[ \t]*#[ \t]*define[ \t]+/, "", line)
	if (!match(line, /^[A-Za-z_][A-Za-z0-9_]*/)) next
	name = substr(line, 1, RLENGTH)
	after = substr(line, RLENGTH + 1)
	if (after ~ /^\(/) next        # function-like macro
	if (name ~ /^_/) next          # include guard / internal
	val = after; sub(/^[ \t]*/, "", val)
	print name "\t" (val ~ /^"/ ? "STR" : "INT")
}
' "$hdrs"/*.h | LC_ALL=C sort -u > "$tmp/macros.txt"

# --- members.tsv : struct members in declaration order --------------------
# One row per member: struct, name, sign(u|s), width(bytes), count, ispad,
# isbytes. The codegen-safe linter guarantees each member is __u8..__u64 /
# __s8..__s64 with at most one array dimension, so this parse is total.
awk '
/^struct[ \t]+[A-Za-z_][A-Za-z0-9_]*[ \t]*\{/ {
	s = $0; sub(/^struct[ \t]+/, "", s); sub(/[ \t]*\{.*/, "", s)
	cur = s; next
}
cur != "" && /^[ \t]*\}/ { cur = ""; next }
cur != "" && /^[ \t]*__[us](8|16|32|64)[ \t]/ {
	line = $0; sub(/^[ \t]*/, "", line)
	n = split(line, a, /[ \t]+/)
	type = a[1]; rest = a[2]
	sign = substr(type, 3, 1)        # u or s
	width = substr(type, 4) / 8      # bytes
	name = rest; sub(/[\[;].*/, "", name)
	count = 1
	if (rest ~ /\[[0-9]+\]/) {
		d = rest; sub(/^[^\[]*\[/, "", d); sub(/\].*/, "", d); count = d + 0
	}
	ispad = (name ~ /^_/) ? 1 : 0
	isbytes = (width == 1 && count > 1) ? 1 : 0
	print cur "\t" name "\t" sign "\t" width "\t" count "\t" ispad "\t" isbytes
}
' "$hdrs"/*.h > "$tmp/members.tsv"

# --- constgen : #define values, evaluated by the C compiler ---------------
# Each macro prints one `M.NAME = value` line. Integers print as decimal
# while they fit a Lua 5.4 signed 64-bit integer, and as hex once the high
# bit is set — a hex literal wraps mod 2^64 in Lua, preserving the bit
# pattern of a mask, whereas an overflowing decimal literal would silently
# become a float.
{
	echo '#include <pkm/pkm.h>'
	echo '#include <stdio.h>'
	echo 'int main(void)'
	echo '{'
	while IFS=$'\t' read -r name kind; do
		if [ "$kind" = STR ]; then
			printf '\tprintf("M.%%s = \\"%%s\\"\\n", "%s", %s);\n' "$name" "$name"
		else
			printf '\t{ unsigned long long v = (unsigned long long)(%s); if (v < 0x8000000000000000ULL) printf("M.%%s = %%llu\\n", "%s", v); else printf("M.%%s = 0x%%llx\\n", "%s", v); }\n' \
				"$name" "$name" "$name"
		fi
	done < "$tmp/macros.txt"
	printf '\treturn 0;\n'
	echo '}'
} > "$tmp/constgen.c"
"$cc" -std=c11 -I"$uapi" "$tmp/constgen.c" -o "$tmp/constgen"

# --- layoutgen : sizeof(struct) and offsetof(member), from the compiler ---
{
	echo '#include <pkm/pkm.h>'
	echo '#include <stdio.h>'
	echo '#include <stddef.h>'
	echo 'int main(void)'
	echo '{'
	while read -r s; do
		[ -n "$s" ] || continue
		printf '\tprintf("SIZE\\t%s\\t%%zu\\n", sizeof(struct %s));\n' "$s" "$s"
	done <<< "$structs"
	while IFS=$'\t' read -r st nm _sign _w _c _p _b; do
		printf '\tprintf("OFF\\t%s\\t%s\\t%%zu\\n", offsetof(struct %s, %s));\n' \
			"$st" "$nm" "$st" "$nm"
	done < "$tmp/members.tsv"
	printf '\treturn 0;\n'
	echo '}'
} > "$tmp/layoutgen.c"
"$cc" -std=c11 -I"$uapi" "$tmp/layoutgen.c" -o "$tmp/layoutgen"
"$tmp/layoutgen" > "$tmp/layout.tsv"

# --- assemble M.struct from members.tsv + layout.tsv ----------------------
# For each struct, walk members in declaration order. Between members emit
# `x` padding to reach the compiler-reported offset (catching any implicit
# hole, not just declared _pad members); declared padding members emit `x`
# for their whole width; real members emit a string.pack code and are
# recorded in `fields` / `field`. A trailing pad reaches sizeof(struct). The
# byte arithmetic is cross-checked against sizeof by the self-test below.
gen_structs() {
	awk '
	function rep(s, n,   r, i) { r = ""; for (i = 0; i < n; i++) r = r s; return r }
	# members.tsv first
	FNR == NR {
		st = $1
		if (!(st in seen)) { seen[st] = 1; order[++norder] = st }
		k = ++cnt[st]
		mname[st, k]  = $2; msign[st, k]  = $3; mwidth[st, k] = $4
		mcount[st, k] = $5; mpad[st, k]   = $6
		mbytes[st, k] = $7
		next
	}
	# layout.tsv second
	$1 == "SIZE" { ssize[$2] = $3; next }
	$1 == "OFF"  { moff[$2 SUBSEP $3] = $4; next }
	END {
		# deterministic struct order (matches the sorted `structs` list)
		for (i = 2; i <= norder; i++) {
			key = order[i]; j = i - 1
			while (j >= 1 && order[j] > key) { order[j + 1] = order[j]; j-- }
			order[j + 1] = key
		}
		for (oi = 1; oi <= norder; oi++) {
			st = order[oi]; n = cnt[st]
			fmt = "<"; cursor = 0; flist = ""; fmap = ""
			for (i = 1; i <= n; i++) {
				nm = mname[st, i]; off = moff[st SUBSEP nm] + 0
				w = mwidth[st, i] + 0; c = mcount[st, i] + 0
				msize = w * c
				pad = off - cursor
				if (pad < 0) {
					printf "gen.sh: %s.%s: negative pad (overlap)\n", st, nm > "/dev/stderr"
					bad = 1
				} else if (pad > 0) {
					fmt = fmt rep("x", pad)
				}
				if (mpad[st, i] == "1") {
					fmt = fmt rep("x", msize)
				} else if (mbytes[st, i] == "1") {
					fmt = fmt "c" c
					flist = flist "\"" nm "\", "
					fmap = fmap sprintf("      %s = {offset = %d, size = %d, signed = false, kind = \"bytes\"},\n", nm, off, msize)
				} else {
					code = (msign[st, i] == "u" ? "I" : "i") w
					sgn = (msign[st, i] == "u" ? "false" : "true")
					kind = (msign[st, i] == "u" ? "uint" : "int")
					if (c > 1) {
						fmt = fmt rep(code, c)
						flist = flist "\"" nm "\", "
						fmap = fmap sprintf("      %s = {offset = %d, size = %d, signed = %s, kind = \"%s\", count = %d},\n", nm, off, msize, sgn, kind, c)
					} else {
						fmt = fmt code
						flist = flist "\"" nm "\", "
						fmap = fmap sprintf("      %s = {offset = %d, size = %d, signed = %s, kind = \"%s\"},\n", nm, off, w, sgn, kind)
					}
				}
				cursor = off + msize
			}
			tail = ssize[st] - cursor
			if (tail < 0) {
				printf "gen.sh: %s: size %d < packed %d\n", st, ssize[st], cursor > "/dev/stderr"
				bad = 1
			} else if (tail > 0) {
				fmt = fmt rep("x", tail)
			}
			sub(/, $/, "", flist)
			printf "  [\"%s\"] = {\n", st
			printf "    size = %d,\n", ssize[st]
			printf "    pack = \"%s\",\n", fmt
			printf "    fields = {%s},\n", flist
			printf "    field = {\n%s    },\n", fmap
			printf "  },\n"
		}
		if (bad) exit 3
	}
	' "$tmp/members.tsv" "$tmp/layout.tsv"
}

# --- write the module -----------------------------------------------------
{
	printf '%s\n' "$banner"
	cat <<'EOF'
--
-- PKM userspace ABI for Lua (provium test authoring).
--
-- M.NAME       every object-like #define from the PKM headers.
-- M.struct     struct layouts as Lua 5.4 string.pack formats:
--                .size    sizeof(struct) in bytes
--                .pack    little-endian format with explicit padding;
--                         feed to provium:pack / provium:unpack
--                .fields  packed value order (padding members omitted)
--                .field   name -> {offset, size, signed, kind[, count]}
--
-- Pack a struct, fire it through a raw vm:syscall, unpack the reply:
--
--   local u = ...  -- this module
--   local s = u.struct.kacs_query_args
--   local buf = provium:pack(s.pack, token_class, buf_len, buf_ptr)
--   local r = vm:syscall(u.SYS_KACS_QUERY, { bufs = {buf}, ptrs = {0} })
EOF
	echo '--'
	echo
	echo 'local M = {}'
	echo
	echo '-- Constants.'
	"$tmp/constgen"
	echo
	echo '-- Struct layouts.'
	echo 'M.struct = {'
	gen_structs
	echo '}'
	echo
	echo 'return M'
} > "$tmp/pkm.lua"

# --- self-check: packsize(pack) == size, if a Lua 5.4 interpreter exists ---
lua_bin=""
for cand in lua5.4 lua54 lua; do
	if command -v "$cand" >/dev/null 2>&1; then
		# luajit and Lua 5.1 lack string.pack; require it.
		if "$cand" -e 'assert(string.pack)' >/dev/null 2>&1; then
			lua_bin="$cand"; break
		fi
	fi
done
if [ -n "$lua_bin" ]; then
	if ! PKM_LUA_CHECK="$tmp/pkm.lua" "$lua_bin" -e '
		local M = dofile(assert(os.getenv("PKM_LUA_CHECK")))
		local n = 0
		for name, s in pairs(M.struct) do
			local ok, sz = pcall(string.packsize, s.pack)
			assert(ok, name .. ": bad pack format: " .. tostring(sz))
			assert(sz == s.size, string.format("%s: packsize %d ~= size %d", name, sz, s.size))
			n = n + 1
		end
		io.stderr:write(string.format("gen.sh: self-check ok (%d struct layouts)\n", n))
	'; then
		echo "gen.sh: self-check FAILED" >&2
		exit 4
	fi
else
	echo "gen.sh: no Lua 5.4 interpreter on PATH — skipping packsize self-check" >&2
fi

# --- install or check -----------------------------------------------------
n_struct="$(printf '%s\n' "$structs" | grep -c .)"
n_const="$(grep -c . "$tmp/macros.txt")"
if [ "$mode" = check ]; then
	if diff -u "$here/pkm.lua" "$tmp/pkm.lua"; then
		echo "gen.sh --check: pkm.lua up to date ($n_struct structs, $n_const constants)"
	else
		echo "gen.sh --check: pkm.lua is stale -- rerun gen.sh" >&2
		exit 1
	fi
else
	cp "$tmp/pkm.lua" "$here/pkm.lua"
	echo "gen.sh: wrote pkm.lua ($n_struct structs, $n_const constants)"
fi
