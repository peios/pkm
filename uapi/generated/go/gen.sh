#!/usr/bin/env bash
# Regenerate the PKM UAPI Go bindings from the C headers.
#
# The headers in pkm/uapi/pkm/ are the single source of truth for the PKM
# ABI. This script mirrors them into two committed, pure-Go files:
#
#   ztypes.go   struct layouts, via `cgo -godefs`. The C compiler measures
#               every struct and the output is plain Go — no cgo at run
#               time, which is what Peios's static binaries require.
#   zconst.go   every object-like #define, evaluated by the C compiler so
#               that ioctl macros (_IOWR ...), shift expressions, and macro
#               references all come out as plain Go constants.
#
# Coverage is driven by the headers themselves — every `struct NAME {` and
# every `#define NAME` is picked up — so a new ABI item cannot be silently
# left unbound. Both outputs carry the standard "DO NOT EDIT" banner.
#
# The output is architecture-independent: the headers use only fixed-width
# types (the codegen-safe linter enforces this), so every struct has the
# same layout on every arch. One generated file serves all of them, and
# this script can run on any host without a cross-compiler.
#
# Usage:
#   gen.sh            regenerate ztypes.go and zconst.go in place
#   gen.sh --check    regenerate into a temp dir and diff against the
#                     committed files; exit non-zero if they differ and
#                     write nothing. This is the CI drift gate.
set -euo pipefail

mode="generate"
case "${1:-}" in
	--check) mode="check" ;;
	"")      ;;
	*)       echo "usage: gen.sh [--check]" >&2; exit 2 ;;
esac

here="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"   # pkm/uapi/generated/go
uapi="$(cd "$here/../.." && pwd)"                         # pkm/uapi
hdrs="$uapi/pkm"
cc="${CC:-cc}"
banner="// Code generated by gen.sh; DO NOT EDIT."

# Precondition: a header outside the codegen-safe subset can make
# `cgo -godefs` reproduce a struct at the wrong size. Fail before that.
bash "$uapi/check-codegen-safe.sh" >/dev/null

tmp="$(mktemp -d)"
trap 'rm -rf "$tmp"' EXIT

# --- ztypes.go : struct layouts via `cgo -godefs` ------------------------
#
# defs.go lists `type Name C.struct_name` for every struct in the headers;
# the Go type name is the C tag with its first letter capitalised. Then an
# awk pass post-processes the cgo -godefs output:
#
#   * Field names are restored from the C headers. cgo -godefs strips a
#     common field-name prefix (its fieldPrefix heuristic, intended for
#     tv_sec/tv_usec -> Sec/Usec), which would otherwise silently rename
#     fields such as result_fd -> Fd. We walk each struct's C members and
#     Go fields in lockstep and put the real names back.
#   * Padding members — those with a leading-underscore C name — become
#     Go's blank identifier `_`, so a consumer cannot read or write ABI
#     padding even by accident.
#
# The lockstep walk also asserts every C member maps to exactly one Go
# field. A count mismatch — e.g. cgo inserting a Pad_cgo_N field for an
# *implicit* hole — fails the build; that is how undeclared padding in a
# header gets caught.
structs="$(grep -hE '^struct[ \t]+[A-Za-z_][A-Za-z0-9_]*[ \t]*\{' "$hdrs"/*.h \
	| sed -E 's/^struct[ \t]+([A-Za-z_][A-Za-z0-9_]*).*/\1/' | LC_ALL=C sort -u)"

{
	echo "//go:build ignore"
	echo
	echo "package uapi"
	echo
	echo "/*"
	echo "#include <pkm/pkm.h>"
	echo "*/"
	echo 'import "C"'
	echo
	while read -r s; do
		cap="$(printf %.1s "$s" | tr '[:lower:]' '[:upper:]')"
		printf 'type %s%s C.struct_%s\n' "$cap" "${s#?}" "$s"
	done <<< "$structs"
} > "$tmp/defs.go"

# C struct members, in declaration order — the rename pass walks these.
awk '
$0 ~ /^struct[ \t]+[A-Za-z_][A-Za-z0-9_]*[ \t]*\{/ {
	s = $0; sub(/^struct[ \t]+/, "", s); sub(/[ \t]*\{.*/, "", s)
	cur = s; next
}
cur != "" && $0 ~ /^\}/ { cur = ""; next }
cur != "" && match($0, /^[ \t]*__[us](8|16|32|64)[ \t]+/) {
	name = substr($0, RSTART + RLENGTH)
	sub(/[^A-Za-z0-9_].*/, "", name)
	print cur "\t" name
}
' "$hdrs"/*.h > "$tmp/members.txt"

# cgo -godefs writes _obj/ into the cwd and echoes its own (absolute-path)
# command line into the output — run it in the temp dir, drop that echo,
# add a stable banner, restore field names, then format.
( cd "$tmp" && go tool cgo -godefs -- -I"$uapi" defs.go ) \
	| sed -n '/^package /,$p' \
	| { printf '%s\n\n' "$banner"; cat; } \
	| awk '
		function cap(s) {
			if (s == "") return s
			if (substr(s, 1, 1) == "_") return "X" s
			return toupper(substr(s, 1, 1)) substr(s, 2)
		}
		NR == FNR { g = cap($1); n[g]++; member[g, n[g]] = $2; next }
		/^type [A-Za-z_][A-Za-z0-9_]* struct \{/ {
			cur = $2; idx = 0; print; next
		}
		cur != "" && /^\}/ {
			if (idx != n[cur]) {
				printf "gen.sh: %s: %d Go fields, %d C members\n", \
					cur, idx, n[cur] > "/dev/stderr"
				bad = 1
			}
			cur = ""; print; next
		}
		cur != "" {
			idx++
			c = member[cur, idx]
			if (c == "") {
				printf "gen.sh: %s: unexpected extra Go field %s\n", \
					cur, $1 > "/dev/stderr"
				bad = 1; print; next
			}
			gotype = $0
			sub(/^[ \t]*[A-Za-z_][A-Za-z0-9_]*[ \t]+/, "", gotype)
			printf "\t%s %s\n", (c ~ /^_/ ? "_" : cap(c)), gotype
			next
		}
		{ print }
		END { if (bad) exit 3 }
	' "$tmp/members.txt" - \
	| gofmt > "$tmp/ztypes.go"

# --- zconst.go : #define constants, evaluated by the C compiler ----------
#
# Collect every object-like macro name — skipping function-like macros and
# the _UAPI_* include guards — then build a C program that prints each as a
# Go constant. The C compiler evaluates _IOWR(...) ioctl encodings, shift
# expressions, and cross-macro references for us. Names are classified
# INT/STR by whether the macro value begins with a double quote.
awk '
{
	line = $0
	while (line ~ /\\[ \t]*$/) {
		sub(/\\[ \t]*$/, "", line)
		if ((getline cont) <= 0) break
		line = line cont
	}
	if (line !~ /^[ \t]*#[ \t]*define[ \t]/) next
	sub(/^[ \t]*#[ \t]*define[ \t]+/, "", line)
	if (!match(line, /^[A-Za-z_][A-Za-z0-9_]*/)) next
	name = substr(line, 1, RLENGTH)
	after = substr(line, RLENGTH + 1)
	if (after ~ /^\(/) next        # function-like macro
	if (name ~ /^_/) next          # include guard / internal
	val = after; sub(/^[ \t]*/, "", val)
	print name "\t" (val ~ /^"/ ? "STR" : "INT")
}
' "$hdrs"/*.h | LC_ALL=C sort -u > "$tmp/macros.txt"

{
	echo '#include <pkm/pkm.h>'
	echo '#include <stdio.h>'
	echo 'int main(void)'
	echo '{'
	printf '\tfputs("%s\\n\\npackage uapi\\n\\nconst (\\n", stdout);\n' "$banner"
	while IFS=$'\t' read -r name kind; do
		if [ "$kind" = STR ]; then
			printf '\tprintf("\\t%%s = \\"%%s\\"\\n", "%s", %s);\n' "$name" "$name"
		else
			printf '\tprintf("\\t%%s = %%llu\\n", "%s", (unsigned long long)(%s));\n' \
				"$name" "$name"
		fi
	done < "$tmp/macros.txt"
	echo '	fputs(")\n", stdout);'
	echo '	return 0;'
	echo '}'
} > "$tmp/dumpconst.c"

"$cc" -std=c11 -I"$uapi" "$tmp/dumpconst.c" -o "$tmp/dumpconst"
"$tmp/dumpconst" | gofmt > "$tmp/zconst.go"

# --- install or check ----------------------------------------------------
n_struct="$(printf '%s\n' "$structs" | grep -c .)"
n_const="$(grep -c . "$tmp/macros.txt")"
status=0
for f in ztypes.go zconst.go; do
	if [ "$mode" = check ]; then
		if ! diff -u "$here/$f" "$tmp/$f"; then
			echo "gen.sh --check: $f is stale -- rerun gen.sh" >&2
			status=1
		fi
	else
		cp "$tmp/$f" "$here/$f"
	fi
done

if [ "$mode" = check ]; then
	[ "$status" -eq 0 ] &&
		echo "gen.sh --check: bindings up to date ($n_struct structs, $n_const constants)"
else
	echo "gen.sh: wrote ztypes.go ($n_struct structs), zconst.go ($n_const constants)"
fi
exit "$status"
